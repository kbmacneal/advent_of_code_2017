function isPrime($number)
{

    if ($number -eq 1) {return $false}
    if ($number -eq 2) {return $true}

    for ($i = 2; $i -le [Math]::Ceiling([Math]::Sqrt($number)); $i++) {
        if($number % $i -eq 0){return $false}
    }

    return $true;

}

